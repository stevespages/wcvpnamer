    <p>
      wcvpNamer is an attempt to provide a useful way to select plant names from the <a href="https://wcvp.science.kew.org/">World Checklist of Vascular Plants</a> (WCVP). The WCVP database holds over a million plant names. The names have been classified by botanists into those that are accepted and others which are synonyms or unplaced. Other names have been classified as artificial hybrids.
    </p>

    <p>
      In many situations, when recording the name of a plant, it would be useful to be sure that the chosen name is an accepted name and not a synonym. In order to help with this, wcvpNamer will suggest names, clearly showing whether or not they are accepted names or synonyms or artificial hybrids. If a name is a synonym it will indicate the accepted name it is synonymous with.
    </p>

    <p>
      wcvpNamer is a purely experimental effort and is in development. The official WCVP website linked to above also provides a search mechanism to access plant names from the database. The website also makes the WCVP database available to download as a csv file. This database was used to make wcvpNamer.
    </p>

